    <h2>Coğrafi Konum</h2>
    <p>Norveç, Kuzey Avrupa'da, İskandinav Yarımadası'nın batısında yer alan bir ülkedir. Batıda Kuzey Denizi, doğuda İsveç, kuzeyde Finlandiya ve Rusya ile komşudur. Norveç'in sahil şeridi, fiyortlar ve dağlarla dolu olup, ülkenin doğal güzellikleriyle ünlüdür.</p>

    <h2>Tarihçe</h2>
    <p>Norveç, tarih boyunca Vikingler tarafından bilinir. 872 yılında Birinci Harald'ın Norveç'i birleştirmesiyle modern Norveç'in temelleri atılmıştır. 1814'te Danimarka'dan ayrılan Norveç, İsveç ile birliğe girmiş, 1905 yılında ise tam bağımsızlığını kazanmıştır. Norveç, II. Dünya Savaşı sırasında Nazi Almanyası tarafından işgal edilmiştir.</p>

    <h2>Kültür ve Dil</h2>
    <p>Norveç'in resmi dili Norveççe'dir ve ülke iki ana dil formunu kullanmaktadır: Bokmål ve Nynorsk. Norveç kültürü, Viking tarihine, açık hava etkinliklerine ve kuzey ışıklarına olan ilgisiyle tanınır. Norveç mutfağı deniz ürünleri, et ve patates gibi malzemeler içerir. Ayrıca, Norveç halk müziği ve edebiyatı da zengin bir geçmişe sahiptir.</p>

    <h2>Ekonomi</h2>
    <p>Norveç, zengin doğal kaynaklara sahip bir ülkedir; petrol ve doğalgaz, ekonomisinin ana bileşenleridir. Ayrıca deniz ürünleri, özellikle balıkçılık, ülkenin önemli ekonomik faaliyetlerinden biridir. Norveç, yüksek yaşam standardı ve güçlü sosyal refah sistemi ile bilinir.</p>

    <h2>Turizm</h2>
    <p>Norveç, doğal güzellikleriyle ünlüdür. Fiyortlar, kuzey ışıkları ve dağlar, turistler için popüler destinasyonlardır. Bergen, Oslo, Tromsø gibi şehirler, turistlerin ilgisini çeken kültürel ve tarihi mekanlar sunar. Ayrıca, doğa yürüyüşleri ve kış sporları gibi açık hava etkinlikleri de oldukça popülerdir.</p>
